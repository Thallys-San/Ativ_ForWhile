package Java_Ativs;

public class Ativ_24 {
    public static void main(String[] args) {
        System.out.println("Sequência de números triangulares até o décimo termo:");
        int soma = 0;
        for (int i = 1; i <= 10; i++) {
            soma += i;
            System.out.print(soma + " ");
        }
    }
}
